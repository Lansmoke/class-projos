#include<stdio.h>
int main() {
	int start;
	int stop;
	int sum;
	
	//Ask user for a starting point
	printf("Enter the starting point: ");
	scanf("%d", &start);
	
	//Ask user to enter end point
	printf("Enter the end point: ");
	scanf("%d", &stop);
	
	//introduce while loop
	while(start<=stop) {
		start++;
		
		//formula for sum
		sum =sum + stop;
	}
	
	//print sum value
	printf("The sum is %d\n", sum);
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	return 0;
}

